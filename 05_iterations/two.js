// While loop to iterate through an array
let myArray = ['flash', 'batman', 'superman'];

let arr = 0;
while (arr < myArray.length) {
    // Uncomment the next line to log each element of the array
    // console.log(`Value is ${myArray[arr]}`);
    arr = arr + 1; // Increment arr by 1
}

// Do-while loop example
let score = 11;

do {
    console.log(`Score is ${score}`);
    score++; // Increment score by 1
} while (score <= 10); // The loop will execute at least once, even if the condition is false
